function request(actionParam, userName, num1, num2, bornYear, m = 1, d = 1, y = 1) {

    return new Promise(resolve => fetch(`http://getpost.itgid.info/index2.php?auth=7859d9d42a8834141d529577207c9596&action=${actionParam}&name=${userName}&num1=${num1}&num2=${num2}&year=${bornYear}&m=${m}&d=${d}&y=${y}`)
        .then(response => resolve(response.text())))
}

function postRequest(actionParam, userName, num1, num2, bornYear, m = 1, d = 1, y = 1) {

    return new Promise(resolve => {

        fetch('http://getpost.itgid.info/index2.php', {

            method: 'POST',
            headers: {
                'Content-type': 'application/x-www-form-urlencoded',
            },
            body: `auth=7859d9d42a8834141d529577207c9596&action=${actionParam}&name=${userName}&num1=${num1}&num2=${num2}&year=${bornYear}&m=${m}&d=${d}&y=${y}`

        })
            .then(response => resolve(response.text()))

    })

}

function promiseArr(out, variable1, variable2) {

    Promise.all([variable1, variable2]).then(value => {
        value.forEach(result => document.querySelector(`.${out}`).textContent += result + ' |');
    })

}

function t1() {

    let get1 = request('1');
    let get2 = request('2', 'Serhii');

    promiseArr('out-1', get1, get2);

}

document.querySelector('.b-1').onclick = t1;

